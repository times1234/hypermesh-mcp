def _now_stamp() -> str:
    return time.strftime("%Y%m%d_%H%M%S")


def _write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def _write_json_if_enabled(enabled: bool, path: Path, payload: Any) -> str | None:
    if not enabled:
        return None
    _write_json(path, payload)
    return str(path)


def _diag(path: Path | None, event: str, **payload: Any) -> None:
    if path is None:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    record = {
        "time": time.strftime("%Y-%m-%d %H:%M:%S"),
        "event": event,
        **payload,
    }
    with path.open("a", encoding="utf-8") as handle:
        handle.write(json.dumps(record, ensure_ascii=False, default=str) + "\n")
        handle.flush()


def _response_diag(response: dict[str, Any] | None, *, tail_chars: int = 2000) -> dict[str, Any]:
    if not isinstance(response, dict):
        return {"success": False, "reason": "no_response"}
    text = str(response.get("response", "") or response.get("stdout", "") or response.get("tail", ""))
    err = str(response.get("error", "") or response.get("message", ""))
    lower = f"{text}\n{err}".lower()
    reason = "ok" if response.get("success") else "failed"
    if "software caused connection abort" in lower or "connection abort" in lower:
        reason = "hypermesh_connection_abort_or_crash"
    elif "connection refused" in lower or "could not connect" in lower:
        reason = "listener_not_available_or_hypermesh_closed"
    elif "timed out" in lower or "timeout" in lower:
        reason = "timeout_or_hypermesh_hang"
    elif "mcp_async_error" in lower:
        reason = "tcl_async_error"
    elif "error" in lower and not response.get("success"):
        reason = "tcl_or_hypermesh_error"
    last_markers = [
        line
        for line in text.splitlines()
        if line.startswith(("MCP_", "MCP_CONSOLE", "PROBE:", "DEBUG_PROBE"))
    ]
    return {
        "success": response.get("success"),
        "status": response.get("status"),
        "reason": reason,
        "message": response.get("message"),
        "error": response.get("error"),
        "elapsed_seconds": response.get("elapsed_seconds"),
        "last_marker": last_markers[-1] if last_markers else "",
        "tail": text[-tail_chars:] if text else "",
    }


def _exception_diag(exc: BaseException) -> dict[str, Any]:
    text = str(exc)
    lower = text.lower()
    reason = "exception"
    if "software caused connection abort" in lower or "connection abort" in lower:
        reason = "hypermesh_connection_abort_or_crash"
    elif "timed out" in lower or "timeout" in lower:
        reason = "timeout_or_hypermesh_hang"
    elif "connection refused" in lower or "could not connect" in lower:
        reason = "listener_not_available_or_hypermesh_closed"
    return {"reason": reason, "type": type(exc).__name__, "message": text}


def _read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace") if path.exists() else ""


def _log(message: str) -> None:
    print(message, flush=True)


def _get_extreme_surface_aspect(info: dict[str, Any], suffix: str = "count") -> Any:
    """Read the renamed extreme-aspect field, with old-log compatibility."""
    return info.get(f"extreme_surface_aspect_{suffix}", info.get(f"surface_aspect_over_100_{suffix}"))


def _set_extreme_surface_aspect(info: dict[str, Any], suffix: str, value: Any) -> None:
    """Store both names so older report code and logs continue to work."""
    info[f"extreme_surface_aspect_{suffix}"] = value
    info[f"surface_aspect_over_100_{suffix}"] = value


def _drag_distance(item: dict[str, Any]) -> float:
    axis = str(item.get("drag_axis") or item.get("axis") or "z").lower()
    dims = item.get("dims") if isinstance(item.get("dims"), dict) else {}
    key = {"x": "dx", "y": "dy", "z": "dz"}.get(axis, "dz")
    return float(dims.get(key) or dims.get("mn") or 0.0)


def _build_drag_solids(results: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    solids: list[dict[str, Any]] = []
    for key, item in sorted(results.items(), key=lambda pair: int(pair[0])):
        if item.get("strategy") != "drag_hex":
            continue
        solids.append(
            {
                "solid_id": int(item["solid_id"]),
                "source_surface_id": int(item.get("source_surface_id", 0)),
                "drag_distance": _drag_distance(item),
                "component_name": str(item["component_name"]),
                "axis": str(item.get("drag_axis") or "z"),
            }
        )
    return solids


def _build_spin_solids(results: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    solids: list[dict[str, Any]] = []
    for key, item in sorted(results.items(), key=lambda pair: int(pair[0])):
        if item.get("strategy") != "spin_hex":
            continue
        solids.append(
            {
                "solid_id": int(item["solid_id"]),
                "component_name": str(item["component_name"]),
                "axis": str(item.get("drag_axis") or item.get("axis") or "z"),
                "element_size": float(item.get("element_size") or 1.0),
                "spin_method": str(item.get("spin_method") or "cutsection"),
                "spin_axis": str(item.get("spin_axis") or item.get("drag_axis") or item.get("axis") or "z"),
                "spin_axis_point": item.get("spin_axis_point"),
                "spin_split_plane_normal": item.get("spin_split_plane_normal"),
                "spin_split_plane_point": item.get("spin_split_plane_point"),
            }
        )
    return solids


