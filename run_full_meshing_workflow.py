def _tcl_braced(value: Any) -> str:
    text = str(value if value is not None else "")
    return "{" + text.replace("\\", "\\\\").replace("}", "\\}") + "}"


def _detect_existing_mesh_by_solid(
    results: dict[str, dict[str, Any]],
    *,
    host: str,
    port: int,
    timeout_seconds: int,
) -> dict[str, dict[str, Any]]:
    solid_ids = [int(key) for key in sorted(results, key=lambda value: int(value))]
    if not solid_ids:
        return {}
    solid_list = " ".join(str(sid) for sid in solid_ids)
    script = f"""
puts "MCP_EXISTING_MESH_BEGIN"
foreach sid {{{solid_list}}} {{
    set comp_name ""
    set elem_count 0
    *createmark components 1 "by solids" $sid
    set cids [hm_getmark components 1]
    if {{[llength $cids] > 0}} {{
        set cid [lindex $cids 0]
        catch {{set comp_name [hm_getvalue components id=$cid dataname=name]}}
        catch {{
            *createmark elements 1 "by comp" "$comp_name"
            set elem_count [llength [hm_getmark elements 1]]
        }}
    }}
    puts "MCP_EXISTING_MESH solid=$sid elems=$elem_count comp=<$comp_name>"
}}
puts "MCP_EXISTING_MESH_END"
""".lstrip()
    response = hm.execute_tcl_gui(
        script,
        host=host,
        port=port,
        timeout_seconds=timeout_seconds,
        enforce_meshing_rules=False,
    )
    text = str(response.get("response", "") or response.get("stdout", ""))
    detected: dict[str, dict[str, Any]] = {}
    for line in text.splitlines():
        match = re.search(r"MCP_EXISTING_MESH solid=(\d+) elems=(\d+) comp=<([^>]*)>", line)
        if not match:
            continue
        sid, elems, comp = match.group(1), int(match.group(2)), match.group(3)
        detected[sid] = {
            "solid_id": int(sid),
            "component_name": comp,
            "element_count": elems,
            "has_existing_mesh": elems > 0,
        }
    return detected


def _filter_tetra_batches_for_active_results(
    classification: dict[str, Any],
    active_results: dict[str, dict[str, Any]],
) -> list[dict[str, Any]]:
    active_ids = {int(key) for key in active_results}
    filtered: list[dict[str, Any]] = []
    next_batch = 1
    for batch in _build_tetra_batches(classification):
        solid_ids = [int(value) for value in batch.get("solid_ids", []) if int(value) in active_ids]
        if not solid_ids:
            continue
        filtered.append(
            {
                **batch,
                "batch": next_batch,
                "solid_ids": solid_ids,
            }
        )
        next_batch += 1
    return filtered


def _run_async_and_wait(
    *,
    script: str,
    log_path: Path,
    host: str,
    port: int,
    enqueue_timeout_seconds: int,
    run_timeout_seconds: int,
    enforce_meshing_rules: bool,
) -> dict[str, Any]:
    start = time.time()
    submit = hm.execute_tcl_gui_async(
        script,
        host=host,
        port=port,
        log_path=str(log_path),
        enqueue_timeout_seconds=enqueue_timeout_seconds,
        enforce_meshing_rules=enforce_meshing_rules,
    )
    result: dict[str, Any] = {
        "submit": submit,
        "log_path": str(log_path),
        "elapsed_seconds": None,
        "status": "submit_error",
        "tail": "",
    }
    if not submit.get("success"):
        result["elapsed_seconds"] = round(time.time() - start, 2)
        return result

    while True:
        text = _read_text(log_path)
        if "MCP_ASYNC_OK" in text:
            result.update(
                {
                    "status": "ok",
                    "elapsed_seconds": round(time.time() - start, 2),
                    "tail": text[-6000:],
                }
            )
            return result
        if "MCP_ASYNC_ERROR" in text:
            result.update(
                {
                    "status": "error",
                    "elapsed_seconds": round(time.time() - start, 2),
                    "tail": text[-6000:],
                }
            )
            return result
        if time.time() - start > run_timeout_seconds:
            result.update(
                {
                    "status": "timeout",
                    "elapsed_seconds": round(time.time() - start, 2),
                    "tail": text[-6000:],
                }
            )
            return result
        time.sleep(10)


def _parse_repair_summary(plan: dict[str, Any]) -> dict[str, Any]:
    repair: dict[str, dict[str, Any]] = {}
    tetra_done: dict[str, dict[str, int]] = {}

    for batch in plan.get("tetra_batches", []):
        text = _read_text(Path(batch["log_path"]))
        current_step: dict[str, str] = {}
        for line in text.splitlines():
            match = re.search(r"MCP_PT_FAIL solid=(\d+) .*delete_tetra_keep_surface_shells=1", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["tetra_deleted_keep_surface_shells"] = 1
                continue

            match = re.search(r"MCP_PT_QUALITY_FAIL solid=(\d+) bad_volume_elements=(\d+) kept_surface_shells=1", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["tetra_deleted_keep_surface_shells"] = 1
                repair.setdefault(sid, {})["bad_volume_elements_when_rolled_back"] = int(match.group(2))
                continue

            match = re.search(
                r"MCP_PT_STOP solid=(\d+) crash_guard_skip_tetmesh shell_count=(\d+) "
                r"limit=(\d+) unfixed_aspect=(\d+) keep_repaired_surface_mesh=1",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["crash_guard_keep_surface_mesh"] = 1
                repair.setdefault(sid, {})["crash_guard_shell_count"] = int(match.group(2))
                repair.setdefault(sid, {})["crash_guard_limit"] = int(match.group(3))
                repair.setdefault(sid, {})["final_bad"] = int(match.group(4))
                repair.setdefault(sid, {})["vol_skew_initial_bad"] = 0
                repair.setdefault(sid, {})["vol_skew_final_bad"] = 0
                repair.setdefault(sid, {})["tetra_not_attempted_reason"] = "crash_guard_skip_tetmesh"
                continue

            match = re.search(
                r"MCP_PT_STOP solid=(\d+) extreme_aspect_skip_tetmesh shell_count=(\d+) "
                r"final_aspect_bad=(\d+) extreme_aspect=(\d+) threshold=([0-9.eE+-]+) keep_repaired_surface_mesh=1",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["extreme_aspect_keep_surface_mesh"] = 1
                repair.setdefault(sid, {})["extreme_aspect_shell_count"] = int(match.group(2))
                repair[sid]["final_bad"] = int(match.group(3))
                _set_extreme_surface_aspect(repair[sid], "count", int(match.group(4)))
                _set_extreme_surface_aspect(repair[sid], "threshold", float(match.group(5)))
                repair.setdefault(sid, {})["vol_skew_initial_bad"] = 0
                repair.setdefault(sid, {})["vol_skew_final_bad"] = 0
                repair.setdefault(sid, {})["tetra_not_attempted_reason"] = "extreme_aspect_skip_tetmesh"
                continue

            match = re.search(
                r"MCP_PT_STOP solid=(\d+) surface_fit_degraded_after_repair "
                r"before=([0-9.eE+-]+) after=([0-9.eE+-]+) fit_tol=([0-9.eE+-]+) "
                r"ratio=([0-9.eE+-]+) limit=([0-9.eE+-]+) "
                r"(?:chord_before=(\d+) chord_after=(\d+) chord_threshold=([0-9.eE+-]+) )?"
                r"action=keep_surface_no_tetra "
                r"shell_count=(\d+) final_aspect_bad=(\d+) keep_repaired_surface_mesh=1",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["surface_fit_degraded_keep_surface_mesh"] = 1
                repair[sid]["surface_fit_before"] = float(match.group(2))
                repair[sid]["surface_fit_after"] = float(match.group(3))
                repair[sid]["surface_fit_tolerance"] = float(match.group(4))
                repair[sid]["surface_fit_ratio"] = float(match.group(5))
                repair[sid]["surface_fit_ratio_limit"] = float(match.group(6))
                if match.group(7) is not None:
                    repair[sid]["surface_chord_dev_before_bad"] = int(match.group(7))
                    repair[sid]["surface_chord_dev_after_bad"] = int(match.group(8))
                    repair[sid]["surface_chord_dev_threshold"] = float(match.group(9))
                repair[sid]["surface_fit_shell_count"] = int(match.group(10))
                repair[sid]["final_bad"] = int(match.group(11))
                repair.setdefault(sid, {})["vol_skew_initial_bad"] = 0
                repair.setdefault(sid, {})["vol_skew_final_bad"] = 0
                repair.setdefault(sid, {})["tetra_not_attempted_reason"] = "surface_fit_degraded_after_repair"
                continue

            match = re.search(
                r"MCP_PT_INFO solid=(\d+) surface_backup_created shell_count=(\d+) "
                r"backup_count=(\d+) component=(\S+)",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["surface_backup_shell_count"] = int(match.group(2))
                repair[sid]["surface_backup_count"] = int(match.group(3))
                repair[sid]["surface_backup_component"] = match.group(4)
                continue

            match = re.search(
                r"MCP_PT_INFO solid=(\d+) surface_backup_restored restored_count=(\d+) "
                r"expected=(\d+) component=(\S+)",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["surface_backup_restored_count"] = int(match.group(2))
                repair[sid]["surface_backup_expected_count"] = int(match.group(3))
                repair[sid]["surface_backup_component"] = match.group(4)
                continue

            match = re.search(
                r"MCP_PT_STOP solid=(\d+) surface_backup_failed shell_count=(\d+) "
                r"backup_count=(\d+) keep_repaired_surface_mesh=1",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["surface_backup_failed_keep_surface_mesh"] = 1
                repair[sid]["surface_backup_shell_count"] = int(match.group(2))
                repair[sid]["surface_backup_count"] = int(match.group(3))
                repair.setdefault(sid, {})["final_bad"] = int(repair.get(sid, {}).get("final_bad") or 0)
                repair.setdefault(sid, {})["vol_skew_initial_bad"] = 0
                repair.setdefault(sid, {})["vol_skew_final_bad"] = 0
                repair.setdefault(sid, {})["tetra_not_attempted_reason"] = "surface_backup_failed"
                continue

            match = re.search(
                r"MCP_PT_INFO solid=(\d+) repaired_surface_mesh_ready shell_count=(\d+) "
                r"final_aspect_bad=(\d+)(?: normal_aspect_bad=(\d+))?"
                r"(?: (?:extreme_surface_aspect|extreme_aspect_over_100)=(\d+))?",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["pre_tetra_surface_shell_count"] = int(match.group(2))
                repair.setdefault(sid, {})["final_bad"] = int(match.group(3))
                repair[sid]["final_bad"] = int(match.group(3))
                if match.group(5) is not None:
                    _set_extreme_surface_aspect(repair[sid], "count", int(match.group(5)))
                    _set_extreme_surface_aspect(
                        repair[sid],
                        "threshold",
                        float(getattr(hm, "TETRA_FATAL_SURFACE_ASPECT", 2000.0)),
                    )
                continue

            match = re.search(
                r"MCP_PT_INFO solid=(\d+) rollback_surface_quality shell_count=(\d+) "
                r"normal_aspect_bad=(\d+) (?:extreme_surface_aspect|extreme_aspect_over_100)=(\d+) "
                r"total_aspect_bad=(\d+)",
                line,
            )
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["rollback_surface_shell_count"] = int(match.group(2))
                repair.setdefault(sid, {})["final_bad"] = int(match.group(5))
                repair[sid]["final_bad"] = int(match.group(5))
                _set_extreme_surface_aspect(repair[sid], "count", int(match.group(4)))
                _set_extreme_surface_aspect(
                    repair[sid],
                    "threshold",
                    float(getattr(hm, "TETRA_FATAL_SURFACE_ASPECT", 2000.0)),
                )
                continue

            match = re.search(
                r"MCP_PT_WARN solid=(\d+) (?:extreme_surface_aspect|surface_aspect_over_100)_unrepaired "
                r"count=(\d+) threshold=([0-9.eE+-]+) shell_count=(\d+)",
                line,
            )
            if match:
                sid = match.group(1)
                info = repair.setdefault(sid, {})
                if _get_extreme_surface_aspect(info, "initial_count") is None:
                    _set_extreme_surface_aspect(info, "initial_count", int(match.group(2)))
                if _get_extreme_surface_aspect(info, "count") is None:
                    _set_extreme_surface_aspect(info, "count", int(match.group(2)))
                if _get_extreme_surface_aspect(info, "threshold") is None:
                    _set_extreme_surface_aspect(info, "threshold", float(match.group(3)))
                if _get_extreme_surface_aspect(info, "shell_count") is None:
                    _set_extreme_surface_aspect(info, "shell_count", int(match.group(4)))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) rollback_keep_surface_mesh=1 shell_count=(\d+)", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["rollback_kept_surface_mesh"] = 1
                repair.setdefault(sid, {})["rollback_surface_shell_count"] = int(match.group(2))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) aspect_bad_local=(\d+)", line)
            if match:
                sid, bad = match.group(1), int(match.group(2))
                repair.setdefault(sid, {})["initial_bad"] = bad
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) aspect_repair=(\w+) count=(\d+)", line)
            if match:
                sid, step, count = match.group(1), match.group(2), int(match.group(3))
                current_step[sid] = step
                repair.setdefault(sid, {})[step] = {"before_count": count}
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) aspect_repair_fast_path=replace_nodes .*sliver_count=(\d+) total=(\d+)", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["replace_nodes_fast_path"] = {
                    "sliver_count": int(match.group(2)),
                    "total": int(match.group(3)),
                }
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) aspect_repair_after before=(\d+) after=(\d+)", line)
            if match:
                sid, before, after = match.group(1), int(match.group(2)), int(match.group(3))
                step = current_step.get(sid, "unknown")
                repair.setdefault(sid, {})[step] = {
                    "before": before,
                    "after": after,
                    "repaired": max(0, before - after),
                }
                repair[sid]["final_bad"] = after
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) aspect_repair_smart_skip=([A-Za-z0-9_]+) reason=([A-Za-z0-9_]+)", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {}).setdefault("smart_repair_skips", []).append(
                    {
                        "skip": match.group(2),
                        "reason": match.group(3),
                    }
                )
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) replace_nodes_changed=(\d+)", line)
            if match:
                repair.setdefault(match.group(1), {})["replace_nodes_changed"] = int(match.group(2))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) replace_nodes_extra_changed=(\d+)", line)
            if match:
                repair.setdefault(match.group(1), {})["replace_nodes_extra_changed"] = int(match.group(2))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) local_remesh_new_shells=(\d+)", line)
            if match:
                repair.setdefault(match.group(1), {})["local_remesh_new_shells"] = int(match.group(2))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) vol_skew_bad_initial=(\d+) threshold=([0-9.eE+-]+)", line)
            if match:
                sid = match.group(1)
                repair.setdefault(sid, {})["vol_skew_initial_bad"] = int(match.group(2))
                repair.setdefault(sid, {})["vol_skew_threshold"] = float(match.group(3))
                continue

            match = re.search(r"MCP_PT_INFO solid=(\d+) vol_skew_repair=([A-Za-z0-9_]+) count=(\d+)", line)
            if match:
                sid, step, count = match.group(1), match.group(2), int(match.group(3))
                current_step[f"{sid}:vol"] = step
                repair.setdefault(sid, {}).setdefault("vol_skew_repairs", {})[step] = {"before_count": count}
                continue

